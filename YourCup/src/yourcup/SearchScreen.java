    private void searchBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchBtnActionPerformed
        // TODO add your handling code here:

        if (("".equals(searchField.getText()))) {
            JOptionPane.showMessageDialog(this, "You have to enter a URL to search");
            return;
        }
        String temp = searchField.getText();
        if (comboBoxIndex == 0) {
            Controller.drawSingle(Controller.columns, playlistModel, VideoTable, Controller.dvll.searchByURL(temp));
        }
        if (comboBoxIndex == 1) {
            Controller.drawSingle(Controller.columns, playlistModel, VideoTable, Controller.dvll.searchByTitle(temp));
        }
        if (comboBoxIndex == 2) {
            Controller.drawVN(Controller.columns, playlistModel, VideoTable, Controller.dvll.searchByTeam(temp));
        }
        if (comboBoxIndex == 3) {
            Controller.drawVN(Controller.columns, playlistModel, VideoTable, Controller.dvll.searchByPlayer(temp));
        }
        if (comboBoxIndex == 4) {
            Controller.drawVN(Controller.columns, playlistModel, VideoTable, Controller.dvll.searchByAction(Controller.Type.valueOf(temp)));
        }

    }//GEN-LAST:event_searchBtnActionPerformed

