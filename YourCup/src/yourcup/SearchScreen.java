    private void playBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_playBtnActionPerformed
        // TODO add your handling code here:
        if (("".equals(searchField.getText()))) {
            JOptionPane.showMessageDialog(this, "You have to enter a URL to search");
            return;
        }
        String temp = searchField.getText();
        String url = "";
        if (comboBoxIndex == 0) {
            url = Controller.dvll.searchByURL(temp).url;
        }
        if (comboBoxIndex == 1) {
            url = Controller.dvll.searchByTitle(temp).url;
        }
        int selectedIndex = VideoTable.getSelectedRow();
        
        if (comboBoxIndex == 2) {
            if (selectedIndex < 0) {
                JOptionPane.showMessageDialog(this, "You Must select");
                return;
            }
            String title = VideoTable.getValueAt(selectedIndex, 0).toString();
            url = Controller.dvll.searchByTitle(title).url;
            Controller.dvll.searchByTeam(temp);
        }
        if (comboBoxIndex == 3) {
            if (selectedIndex < 0) {
                JOptionPane.showMessageDialog(this, "You Must select");
                return;
            }
            String title = VideoTable.getValueAt(selectedIndex, 0).toString();
            url = Controller.dvll.searchByTitle(title).url;
            Controller.dvll.searchByPlayer(temp);
        }
        if (comboBoxIndex == 4) {
            if (selectedIndex < 0) {
                JOptionPane.showMessageDialog(this, "You Must select");
                return;
            }
            String title = VideoTable.getValueAt(selectedIndex, 0).toString();
            url = Controller.dvll.searchByTitle(title).url;;
            Controller.dvll.searchByAction(Controller.Type.valueOf(temp));
        }
        Controller.EXCUTECMDCOMMAND(url);
    }//GEN-LAST:event_playBtnActionPerformed

